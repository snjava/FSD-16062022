public class Operator1
{
	public static void main(String ar[])
	{
		int a = 10;
		int b = 3;
		int ans = a + b;
		System.out.println(ans);	// 13

		int ans2 = a - b;
		System.out.println(ans2);	// 7

		int ans3 = a * b;
		System.out.println(ans3);	// 30

		System.out.println(a / b);	// 3

		System.out.println(a % b);	// 1
	}
}